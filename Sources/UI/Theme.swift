import SwiftUI

enum Theme {
    static let darkBackground = Color(hex: "121212")
    static let surfaceDark = Color(hex: "1E1E1E")
    static let primaryOrange = Color(hex: "FF5722")
    static let secondaryOrange = Color(hex: "FF8A65")
    static let textPrimary = Color.white
    static let textSecondary = Color(hex: "B3B3B3")
    static let error = Color(hex: "CF6679")
    static let success = Color(hex: "4CAF50")
}

extension Color {
    init(hex: String) {
        let hex = hex.trimmingCharacters(in: CharacterSet.alphanumerics.inverted)
        var int: UInt64 = 0
        Scanner(string: hex).scanHexInt64(&int)
        let a, r, g, b: UInt64
        switch hex.count {
        case 3: // RGB (12-bit)
            (a, r, g, b) = (255, (int >> 8) * 17, (int >> 4 & 0xF) * 17, (int & 0xF) * 17)
        case 6: // RGB (24-bit)
            (a, r, g, b) = (255, int >> 16, int >> 8 & 0xFF, int & 0xFF)
        case 8: // ARGB (32-bit)
            (a, r, g, b) = (int >> 24, int >> 16 & 0xFF, int >> 8 & 0xFF, int & 0xFF)
        default:
            (a, r, g, b) = (255, 0, 0, 0)
        }
        self.init(
            .sRGB,
            red: Double(r) / 255,
            green: Double(g) / 255,
            blue:  Double(b) / 255,
            opacity: Double(a) / 255
        )
    }
}